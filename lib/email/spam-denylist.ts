/**
 * Normalize an email address by stripping the +tag from the local part.
 * Used so that spam domain checks work regardless of + aliases.
 * Does NOT block + aliases — they are legitimate.
 */
export function normalizeEmail(email: string): string {
  const atIndex = email.lastIndexOf("@");
  if (atIndex < 0) return email;
  const local = email.slice(0, atIndex);
  const domain = email.slice(atIndex + 1);
  const normalized = local.split("+")[0];
  return `${normalized}@${domain}`.toLowerCase();
}

/**
 * Returns true when the local part of the email contains a + alias (e.g. user+tag@example.com).
 */
export function hasPlusAlias(email: string): boolean {
  const atIndex = email.lastIndexOf("@");
  if (atIndex < 0) return false;
  return email.slice(0, atIndex).includes("+");
}

/**
 * Extract the domain portion of an email address, lowercased and trimmed.
 * Returns empty string for invalid inputs.
 */
export function extractEmailDomain(email: string): string {
  if (!email || !email.includes("@")) return "";
  return email.slice(email.lastIndexOf("@") + 1).toLowerCase().trim();
}

/**
 * Check whether an email address belongs to a known spam/disposable domain.
 * Returns true if the domain (or any parent domain) is on the denylist.
 * The check is case-insensitive. + aliases are stripped before the domain check.
 */
export function isSpamDomain(email: string): boolean {
  const domain = extractEmailDomain(normalizeEmail(email));

  if (!domain) {
    return false;
  }

  if (SPAM_DOMAINS.has(domain)) {
    return true;
  }

  // Also check parent domains (e.g. sub.mailinator.com → mailinator.com)
  const parts = domain.split(".");
  for (let i = 1; i < parts.length - 1; i++) {
    const parentDomain = parts.slice(i).join(".");
    if (SPAM_DOMAINS.has(parentDomain)) {
      return true;
    }
  }

  return false;
}
